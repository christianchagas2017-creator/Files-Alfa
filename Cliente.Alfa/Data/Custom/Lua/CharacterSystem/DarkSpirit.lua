function RenderModel(BMDStruct, ObjectStruct, ItemIndex)
    local Bmd = BMD.new(BMDStruct)
    local Obj = Object.new(ObjectStruct)

    local getRenderModel = DARK_SPIRIT_RENDER_MODEL[ItemIndex]

    if getRenderModel ~= nil then
		if getRenderModel and getRenderModel.Spark then
			for _, spark in ipairs(getRenderModel.Spark) do
				for _, action in ipairs(spark.Actions) do
					if (action == -1 or action == Obj:getAction()) then
						math.randomseed(worldTime())
						Bmd:TransformPosition3(math.random(spark.MinBone, spark.MaxBone), 0, 0, 0, ObjectStruct)
						if spark.Type == 0 then spark.Type = 5 end
						if spark.Type == 1 then spark.Type = 32 end
						if spark.Type == 2 then spark.Type = 33 end
						if spark.Type == 3 then spark.Type = 34 end
						if spark.Type == 4 then spark.Type = 35 end
						Bmd:CreateParticle2(32049, spark.Type, spark.Size, spark.Color[1], spark.Color[2], spark.Color[3], ObjectStruct)
						--Bmd:CreateParticle(32049, spark.Type, spark.Size, spark.Color[1], spark.Color[2], spark.Color[3], ObjectStruct)
					end
				end
			end
		end

		Obj:setScale(getRenderModel.Scale)

		local RenderBmd = DARK_SPIRIT_RENDER_MODEL[ItemIndex].Bmd
        for i = 1, #RenderBmd do
			local glow = RenderBmd[i]

			--math.randomseed(os.time())
			math.randomseed(worldTime())

			local randValue = 100

			if glow.Timer ~= 100 then
				randValue = math.random(1, 100)
			end

			if glow.Timer >= randValue then
				Bmd:SetLight(glow.R, glow.G, glow.B)
				Bmd:glColor3fv()

				if glow.Render then
					if glow.Effect == 0 then
						local Vertex = GetDoubleRender(glow.RenSpeed, 0.0) + glow.RenOpacity
						if Vertex > glow.RenColor
						then
							Vertex = glow.RenColor
						end

						if glow.Color then
							if glow.R ~= -1 then
								Bmd:SetLight(glow.RR, Vertex, Vertex)
							end
							if glow.G ~= -1 then
								Bmd:SetLight(Vertex, glow.RG, Vertex)
							end
							if glow.B ~= -1 then
								Bmd:SetLight(Vertex, Vertex, glow.RB)
							end
						else
							Bmd:SetLight(Vertex, Vertex, Vertex)
						end

						Bmd:glColor3fv()

						Bmd:RenderMesh(glow.Mesh, glow.Glow, Obj:Alpha(), Obj:Mesh(), Obj:Light(), Obj:TexCoordU(), Obj:TexCoordV(), glow.Texture)
					else
						local AnyTex = worldTime()
						local TexCoordV = Obj:TexCoordV()
						local TexCoordU = Obj:TexCoordU()

						if glow.Effect == 1 then TexCoordV = AnyTex / 100 * glow.RenSpeed end
						if glow.Effect == 2 then TexCoordV = AnyTex / 100 * -glow.RenSpeed end
						if glow.Effect == 3 then TexCoordU = AnyTex / 100 * glow.RenSpeed end
						if glow.Effect == 4 then TexCoordU = AnyTex / 100 * -glow.RenSpeed end
						if glow.Effect == 5 then
							TexCoordU = (math.floor((AnyTex % (15 * 40) / 40) % 4)) * glow.RenSpeed
							TexCoordV = (math.floor((AnyTex % (15 * 40) / 40) / 4)) * glow.RenSpeed
						end
						if glow.Effect == 6 then
							TexCoordU = (math.floor((AnyTex % (15 * 40) / 40) % 4)) * -glow.RenSpeed
							TexCoordV = (math.floor((AnyTex % (15 * 40) / 40) / 4)) * -glow.RenSpeed
						end

						if glow.FixEffect then
							Bmd:SetLight(0.0, 0.0, 0.0)
							Bmd:glColor3fv()
							Bmd:RenderMesh(glow.Mesh, 1, Obj:Alpha(), glow.Mesh, Obj:Light(), Obj:TexCoordU(), Obj:TexCoordV(), glow.Texture)
						end

						Bmd:SetLight(glow.R, glow.G, glow.B)
						Bmd:glColor3fv()

						for _, action in ipairs(glow.Actions) do
							if (action == -1 or action == Obj:getAction()) then
								for c = 1, glow.Value do
									Bmd:RenderMesh(glow.Mesh, glow.Glow, Obj:Alpha(), Obj:Mesh(), Obj:Light(), TexCoordU, TexCoordV, glow.Texture)
								end
							end
						end
					end
				else
					for _, action in ipairs(glow.Actions) do
						if (action == -1 or action == Obj:getAction()) then
							for c = 1, glow.Value do
								Bmd:RenderMesh(glow.Mesh, glow.Glow, Obj:Alpha(), Obj:Mesh(), Obj:Light(), Obj:TexCoordU(), Obj:TexCoordV(), glow.Texture)
							end
						end
					end
				end
			end

			if glow.Shadow then
				--Bmd:RenderShadowModel()
				--ou
				Bmd:SetLight(0.0, 0.0, 0.0)
				Bmd:glColor3fv()
				Bmd:RenderMesh(glow.Mesh, 1185, 0.9, Obj:Mesh(), Obj:Light(), Obj:TexCoordU(), Obj:TexCoordV(), -1) --OPACIDADE DO ALPHA 0.9
			end

			local bmdEffect = DARK_SPIRIT_RENDER_MODEL[ItemIndex]

			if bmdEffect ~= nil then
				local RenderEffect = DARK_SPIRIT_RENDER_MODEL[ItemIndex].Effect
				for i = 1, #RenderEffect do
					local effect = RenderEffect[i]
					local randValue = 100

					if effect.RandTime ~= 100 then
						randValue = math.random(1, 100)
					end

					local black = 0
					if effect.Black then black = 1 end

					if effect.RandTime >= randValue then
						for _, action in ipairs(effect.Actions) do
							if action == -1 or action == Obj:getAction() then
								Bmd:TransformPosition3(effect.Bone, effect.PosX, effect.PosY, effect.PosZ, ObjectStruct)
								if (effect.EffectType == 0) then
									Bmd:CreateSprite(effect.EffectIndex, effect.Size, effect.R, effect.G, effect.B, ObjectStruct, black)
								elseif (effect.EffectType == 1) then
									Bmd:CreateParticle(effect.EffectIndex, effect.EffectLevel, effect.Size, effect.R, effect.G, effect.B, ObjectStruct)
								elseif (effect.EffectType == 2) then
									Bmd:CreateEffect(effect.EffectIndex, effect.EffectLevel, effect.R, effect.G, effect.B, ObjectStruct)
								end
							end
						end
					end
				end
			end
		end
    else
		Bmd:SetLight(1.0, 1.0, 1.0)
		Bmd:glColor3fv()
        Bmd:RenderBody(2, Obj:Alpha(), Obj:Mesh(), Obj:Light(), Obj:TexCoordU(), Obj:TexCoordV(), Obj:Hidden(), -1)
    end

    Bmd = nil
    Obj = nil
	collectgarbage()
end