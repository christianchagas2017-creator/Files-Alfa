function StartBuffColorBK()
	for _, itemList in pairs(CUSTOM_BUFF_COLOR_BK) do
		for i = 1, #itemList do
			local item = itemList[i]
			local Black = 0 if item.Black then Black = 1 end

			if i == 1 then
				SetBuffColorBK(item.Color, item.R, item.G, item.B, item.Size, item.Image, Black)
			elseif i == 2 then
				SetBuffColorBK2(item.Color, item.R, item.G, item.B, item.Size, item.Image, Black)
			elseif i == 3 then
				SetBuffColorBK3(item.Color, item.R, item.G, item.B, item.Size, item.Image, Black)
			elseif i == 4 then
				SetBuffColorBK4(item.Color, item.R, item.G, item.B, item.Size, item.Image, Black)
			elseif i == 5 then
				SetBuffColorBK5(item.Color, item.R, item.G, item.B, item.Size, item.Image, Black)
			end
		end
	end
end

function CheckColorBuffBK(charObj)
	local char = Character.new(charObj)

	local itens = {
		[0] = char:getSword(),   -- Espada
		[1] = char:getShield(),  -- Escudo
		[2] = char:getHelm(),    -- Elmo
		[3] = char:getArmor(),
		[4] = char:getPants(),
		[5] = char:getGloves(),
		[6] = char:getBoots(),
		[7] = char:getWing(),    -- Asa
		[8] = char:getHelper()   -- Montaria
	}

	-- ⚠️ Verificação 1: Buff para montaria solo
	local mount = itens[8]
	if mount ~= nil and CUSTOM_BUFF_COLOR_BK[mount] ~= nil then
		for _, item in ipairs(CUSTOM_BUFF_COLOR_BK[mount]) do
			if item.Slot[1] == 8 then -- Verifica se o item é uma montaria (slot 8)
				return item.Color -- Retorna a cor do buff da montaria
			end
		end
	end

	-- ⚠️ Verificação 2: Buff para o conjunto completo
	local sword   = itens[0]
	local shield  = itens[1]
	local helm    = itens[2]
	local wing    = itens[7]
	local mount   = itens[8]

	if sword == nil or shield == nil or helm == nil or wing == nil or mount == nil then
		return 0 -- Não ativa o buff do conjunto se faltar algum item
	end

	if CUSTOM_BUFF_COLOR_BK[sword] == nil then return 0 end
	if CUSTOM_BUFF_COLOR_BK[shield] == nil then return 0 end
	if CUSTOM_BUFF_COLOR_BK[helm] == nil then return 0 end
	if CUSTOM_BUFF_COLOR_BK[wing] == nil then return 0 end
	if CUSTOM_BUFF_COLOR_BK[mount] == nil then return 0 end

	-- ✅ Conjunto completo: percorre os itens para aplicar a cor do buff
	for itemIndex, itemList in pairs(CUSTOM_BUFF_COLOR_BK) do
		for _, item in ipairs(itemList) do
			for _, slot in ipairs(item.Slot) do
				if itens[slot] ~= nil and itens[slot] == itemIndex then
					return item.Color
				end
			end
		end
	end

	return 0 -- Não encontrou buff
end





